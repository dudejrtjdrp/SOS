/** Render a module's guide icon by name, with a safe default. */
export function ModuleIcon({
  name,
  className,
}: {
  name?: string;
  className?: string;
}) {
  const Icon = (name && ICONS[name]) || Wrench;
  return <Icon className={className} aria-hidden />;
}
